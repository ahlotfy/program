let languageSelector = document.querySelector("select");
if (sessionStorage.getItem("lang") === null) {
  sessionStorage.setItem("lang", "en");
}
languageSelector.addEventListener("change", (e) => {
  setLanguage(e.target.value);
  sessionStorage.setItem("lang", e.target.value);
});

document.addEventListener("DOMContentLoaded", () => {
  setLanguage(sessionStorage.getItem("lang"));
});
let setLanguage = (language) => {
  let elements = document.querySelectorAll("[data-transtion]");
  elements.forEach((e) => {
    let translationKey = e.getAttribute("data-transtion");
    e.textContent = translation[language][translationKey];
  });
  let english = document.querySelector("select .en");
  let arabic = document.querySelector("select .ar");
  let submit = document.querySelector(".contact form input[type='submit']");
  if (language === "ar") {
    document.body.style.direction = "rtl";
    arabic.setAttribute("selected", "");
    submit.value = "إرسال";
  } else {
    document.body.style.direction = "ltr";
    english.setAttribute("selected", "");
    submit.value = "Send";
  }
};
